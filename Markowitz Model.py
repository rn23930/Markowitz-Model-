for i in range(n - 1):
    ssigma[i + 1] = ssigma[i] + 2 * random.uniform(0, 1)
    mmu[i + 1] = mmu[i] + 1
  # ssigma and mmu arrays are created for storing volatility and expected return values of assets.
ddiag = np.array([[0] * n for _ in range(n)], dtype=float)
